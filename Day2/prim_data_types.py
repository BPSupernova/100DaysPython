# Math operations
3 + 5   # Addition
7 - 4   # Subtraction
3 * 2   # Multiplication
6 / 3   # Division
2 ** 3  # Exponents

# Rounding numbers
print(round(8/3, 2)) # Rounds up to 2.67 -> 2 decimal places precision via second parameter

# Floor division
print(8 // 3) # Converts down to 2 automatically w/o casting to int

#f-String
f"Your score is {var1}" # Converts everything to string automatically

